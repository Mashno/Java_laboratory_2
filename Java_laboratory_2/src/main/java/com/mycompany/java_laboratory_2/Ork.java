public class Ork {
    private String name;
    private String weapon;
    private String armor;
    private String banner;
    private String additionalItem;
    private int strength;
    private int agility;
    private int intelligence;
    private int health;
    private String tribe;
    public Ork(){
        
    }
    
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    
    public String getWeapon() { return weapon; }
    public void setWeapon(String weapon) { this.weapon = weapon; }
    
    public String getArmor() { return armor; }
    public void setArmor(String armor) { this.armor = armor; }
    
    public String getBanner() { return banner; }
    public void setBanner(String banner) { this.banner = banner; }
    
    public String getAdditionalItem() { return additionalItem; }
    public void setAdditionalItem(String additionalItem) { this.additionalItem = additionalItem; }
    
    public int getStrength() { return strength; }
    public void setStrength(int strength) { this.strength = strength; }
    
    public int getAgility() { return agility; }
    public void setAgility(int agility) { this.agility = agility; }
    
    public int getIntelligence() { return intelligence; }
    public void setIntelligence(int intelligence) { this.intelligence = intelligence; }
    
    public int getHealth() { return health; }
    public void setHealth(int health) { this.health = health; }
    
    public String getTribe() { return tribe; }
    public void setTribe(String tribe) { this.tribe = tribe; }
}
